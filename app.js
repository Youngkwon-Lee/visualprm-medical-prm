function importJSON(input){
  const file=input.files[0];
  if(!file)return;
  const reader=new FileReader();
  reader.onload=function(e){
    try{
      const data=JSON.parse(e.target.result);
      const samples=data.samples||data;
      if(!Array.isArray(samples)){alert('Invalid format. A samples array is required.');return;}
      let count=0;
      samples.forEach(s=>{
        if(!s.steps||!s.steps.length)return;
        DS.push({
          id:s.id||`imp_${Date.now()}_${count}` ,
          case_type:s.case_type||'Unknown',
          modality:s.modality||'',
          question:s.question||'',
          options:s.options||{},
          gold:s.gold||'',
          image_url:s.image_url||'',
          sol_num:s.sol_num||1,
          steps:s.steps.map((st,i)=>({
            step:st.step||i+1,
            title:st.title||`Step ${i+1}`,
            text:st.text||'',
            mc_score:(st.mc_score==null?0:st.mc_score),
            label:st.label||(st.mc_score>0?'+':'-')
          })),
          final_label:s.final_label||(s.steps.every(st=>(st.label||'+')==='+' ) ?'+':'-'),
          conversations:s.conversations||[]
        });
        count++;
      });
      input.value='';
      if(count>0){
        updNav();
        go(4);
        document.getElementById('batchSt').textContent=`${count} items loaded`;
      }else{
        alert('No valid samples were found.');
      }
    }catch(err){
      alert('JSON parse error: '+err.message);
    }
  };
  reader.readAsText(file);
}


