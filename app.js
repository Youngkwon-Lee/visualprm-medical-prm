function qaD(qi,status){
  if(status==='rej'){
    const reason=(qaS.items[qi].rejection_reason||'').trim();
    if(!reason){
      qaReject(qi);
      return;
    }
  }
  const prev=qaS.items[qi].status;
  qaS.items[qi].status=status;
  if(prev!=='pending'){if(prev==='ok')qaS.ok--;else qaS.rej--;qaS.done--;}
  if(status==='ok')qaS.ok++;else qaS.rej++;
  qaS.done++;
  const card=document.getElementById(`qc${qi}`);
  card.style.borderLeftColor=status==='ok'?'var(--ok)':'var(--err)';
  document.getElementById(`qst${qi}`).textContent=status==='ok'?'Approved':'Rejected';
  document.getElementById(`qst${qi}`).className=`tag ${status==='ok'?'tag-ok':'tag-err'}`;
  if(status==='ok'){ const rejDiv=document.getElementById('qrej'+qi); if(rejDiv)rejDiv.classList.remove('show'); }

  updQAGauge();
}

function qaMoveFocus(delta){
  if(!qaS.items.length)return;
  const prev=qaFocus;
  qaFocus=Math.max(0,Math.min(qaS.items.length-1,qaFocus+delta));
  if(prev!==qaFocus){
    const oldCard=document.getElementById(`qc${prev}`);
    if(oldCard)oldCard.classList.remove('focused');
    const oldTag=oldCard?.querySelector('.tag-warn');
    if(oldTag)oldTag.remove();
    const newCard=document.getElementById(`qc${qaFocus}`);
    if(newCard){
      newCard.classList.add('focused');
      const header=newCard.querySelector('div');
      if(header&&!newCard.querySelector('.tag-warn')){
        const ft=document.createElement('span');
        ft.className='tag tag-warn';
        ft.style.marginLeft='auto';
        ft.textContent='Focus';
        header.appendChild(ft);
      }
      newCard.scrollIntoView({behavior:'smooth',block:'nearest'});
    }
  }
}

