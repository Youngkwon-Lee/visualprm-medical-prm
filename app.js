function tokenizeStepVerifierText(text){
  return (text||'').toLowerCase().match(/[a-z]{3,}/g)?.filter(t=>!['the','and','with','from','that','this','there','which','into','about'].includes(t))||[];
}

function heuristicVerifySteps(c, sol){
  const opts=Array.isArray(c.options||c.opts)?(c.options||c.opts):Object.values(c.options||c.opts||{});
  const goldIdx=typeof c.g==='number'?c.g:'ABCD'.indexOf(gold);
  const goldText=opts[goldIdx]||'';
  const qTokens=new Set(tokenizeStepVerifierText(c.question||c.q||''));
  const goldTokens=new Set(tokenizeStepVerifierText(goldText));
  return sol.steps.map((step, idx)=>{
    const tokens=new Set(tokenizeStepVerifierText(`${step.t} ${step.tx}`));
    const overlap=[...tokens].filter(t=>qTokens.has(t)).length;
    const goldOverlap=[...tokens].filter(t=>goldTokens.has(t)).length;
    let score=0.2;
    if((step.tx||'').length>=40)score+=0.15;
    score+=Math.min(0.25, overlap*0.06);
    score+=Math.min(0.25, goldOverlap*0.1);
    if(/placeholder|analysis content|step \d+/.test((step.tx||'').toLowerCase()))score-=0.25;
    if(/weaker|mismatch|partial|nonspecific|uncertain|incomplete/.test((step.tx||'').toLowerCase()))score-=0.12;
    if(idx===sol.steps.length-1){
      const lower=(step.tx||'').toLowerCase();
      if((goldText&&lower.includes(goldText.toLowerCase()))||lower.includes(`answer: ${'abcd'[goldIdx]||''}`))score+=0.2;
      else if(lower.includes('final answer'))score-=0.1;
    }
    score=Math.max(0,Math.min(1,score));
    return {
      score,
      label:score>=0.5?'+':'-',
      rationale:score>=0.5?'Matched question/gold evidence.':'Weak grounding to the question or gold answer.'
    };
  });
}

async function sampleContinuationFromPrefix(c, prefixSteps){
  const payload={
    question:c.question||c.q||'',
    options:Array.isArray(c.options||c.opts)?(c.options||c.opts):Object.values(c.options||c.opts||{}),
    gold:typeof c.g==='number'?c.g:'ABCD'.indexOf(gold),
    case_type:c.case_type||c.nm||'Medical',
    dataset:c.dataset||'pathvqa',
    temperature:PAPER_SAMPLING_TEMPERATURE,
    top_p:PAPER_SAMPLING_TOP_P,
    prefix_steps:prefixSteps.map(step=>({title:step.t||step.title||'Step',text:step.tx||step.text||''}))
  };
  const res=await fetch('http://localhost:8764/generate-steps',{
    method:'POST',
    headers:{'Content-Type':'application/json'},
    body:JSON.stringify(payload)
  });
  if(!res.ok){
    const details=await res.text();
    throw new Error(`rollout failed: ${res.status} ${details}`);
  }
  return await res.json();
}

function rolloutAnswerIsCorrect(c, continuation){
  const goldIdx=typeof c.g==='number'?c.g:'ABCD'.indexOf(gold);
  if(typeof continuation?.final_answer_index==='number')return continuation.final_answer_index===goldIdx;
  const letter=(continuation?.final_answer_letter||'').trim().toUpperCase();
  if(letter && 'ABCD'.includes(letter))return 'ABCD'.indexOf(letter)===goldIdx;
  const allTexts=(continuation?.steps||[]).map(step=>`${step.title||''} ${step.text||''}`).join(' ').toLowerCase();
  const opts=Array.isArray(c.options||c.opts)?(c.options||c.opts):Object.values(c.options||c.opts||{});
  const goldText=(opts[goldIdx]||'').toLowerCase();
  return Boolean(goldText) && allTexts.includes(goldText);
}

async function rolloutSolutionSteps(c, sol, progressCb){
  const k=parseInt(document.getElementById('rK').value);
  const goldIdx=typeof c.g==='number'?c.g:'ABCD'.indexOf(gold);
  if(mcMode!=='api'){
    throw new Error('Paper-style rollout requires OpenAI verifier mode.');
  }
  const results=[];
  for(let idx=0;idx<sol.steps.length;idx++){
    let cnt=0;
    const prefix=sol.steps.slice(0,idx+1);
    for(let ri=0;ri<k;ri++){
      if(progressCb)progressCb(idx, sol.steps.length, ri, k);
      const continuation=await sampleContinuationFromPrefix(c,prefix);
      if(rolloutAnswerIsCorrect(c,continuation))cnt++;
    }
    const score=cnt/k;
    results.push({
      si:idx,
      title:sol.steps[idx].t,
      text:sol.steps[idx].tx,
      cnt,
      k,
      score,
      label:score>0?'+':'-',
      rationale:`Rollout success ${cnt}/${k} toward gold ${String.fromCharCode(65+goldIdx)}.`,
      mode:'rollout'
    });
  }
  return results;
}

async function verifySolutionSteps(c, sol){
  const payload={
    question:c.question||c.q||'',
    options:Array.isArray(c.options||c.opts)?(c.options||c.opts):Object.values(c.options||c.opts||{}),
    gold:typeof c.g==='number'?c.g:'ABCD'.indexOf(gold),
    case_type:c.case_type||c.nm||'Medical',
    modality:c.modality||c.mt||'',
    steps:sol.steps.map(step=>({title:step.t,text:step.tx}))
  };
  if(mcMode==='api'){
    try{
      const res=await fetch('http://localhost:8764/verify-steps',{
        method:'POST',
        headers:{'Content-Type':'application/json'},
        body:JSON.stringify(payload)
      });
      if(res.ok){
        const data=await res.json();
        if(Array.isArray(data.results)&&data.results.length===sol.steps.length){
          return data.results.map((r,idx)=>({
            si:idx,
            title:sol.steps[idx].t,
            text:sol.steps[idx].tx,
            cnt:Math.round((r.score||0)*parseInt(document.getElementById('rK').value)),
            k:parseInt(document.getElementById('rK').value),
            score:Math.max(0,Math.min(1,Number(r.score)||0)),
            label:r.label==='+'?'+':'-',
            rationale:r.rationale||'',
            mode:data.mode||'backend'
          }));
        }
      }
    }catch(e){
      console.warn('Step verifier unavailable, falling back to heuristic:', e);
    }
  }
  const k=parseInt(document.getElementById('rK').value);
  return heuristicVerifySteps(c,sol).map((r,idx)=>({
    si:idx,title:sol.steps[idx].t,text:sol.steps[idx].tx,cnt:Math.round(r.score*k),k,score:r.score,label:r.label,rationale:r.rationale,mode:'heuristic'
  }));
}

