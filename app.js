function annotateTrainingPreviewText(text){
  return text.split('\n').map(line=>{
    const match=line.match(/^(\s*)"([^"]+)":/);
    if(!match)return line;
    const key=match[2];
    const comment=TRAINING_PREVIEW_COMMENTS_KO[key];
    if(!comment)return line;
    return `${line}  # ${comment}`;
  }).join('\n');
}

