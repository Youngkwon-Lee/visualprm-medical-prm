const VISUALPRM_SYSTEM_PROMPT = 'You are an advanced AI assistant, designed to serve as a process supervision model. In this task, I will provide a problem statement followed by the first step of the solution process. For each subsequent turn, I will give you a new step in the solution. Your role is to assess the correctness of the current step by outputting a single correctness token: + for correct and - for incorrect.';

function buildVisualPRMConversations(sample){
  const opts=Array.isArray(sample.options)?sample.options:Object.values(sample.options||{});
  const conversations=[{from:'system',value:VISUALPRM_SYSTEM_PROMPT}];
  sample.steps.forEach((step,idx)=>{
    const stepHeader=idx===0
      ? `${sample.image_url?'<image>\n':''}Question: ${sample.question}\nOptions:\n${opts.map((o,j)=>`${'ABCD'[j]||j}. ${o}`).join('\n')}\nStep ${idx+1}: ${step.text}`
      : `Step ${idx+1}: ${step.text}`;
    conversations.push({from:'human',value:stepHeader});
    conversations.push({from:'gpt',value:(step.label||'+')==='+'?'+':'-'});
  });
  return conversations;
}

function toVisualPRMSample(sample, idx){
  const out={
    id:Number.isFinite(Number(sample.id))?Number(sample.id):idx,
    conversations:buildVisualPRMConversations(sample)
  };
  if(sample.image_url)out.image=sample.image_url;
  return out;
}

