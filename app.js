async function mkSol(c,idx,tot,cGold){
  cGold=cGold||gold;
  const genParams=getGenerationParams(idx);
  const apiRes = await fetch('http://localhost:8764/generate-steps', {
    method: 'POST',
    headers: {'Content-Type': 'application/json'},
    body: JSON.stringify({
      question: c.question || c.q || '',
      options: c.options || c.opts || [],
      gold: cGold === 'A' ? 0 : cGold === 'B' ? 1 : cGold === 'C' ? 2 : cGold === 'D' ? 3 : 0,
      dataset: c.dataset || 'pathvqa',
      case_type: c.case_type || c.nm || 'Medical',
      temperature: genParams.temperature,
      top_p: genParams.top_p
    })
  });

  if(!apiRes.ok){
    const errorText=await apiRes.text();
    throw new Error(`generation failed (${apiRes.status}): ${errorText}`);
  }

  const apiData = await apiRes.json();
  if(!apiData.steps || !apiData.steps.length){
    throw new Error('generation failed: backend returned no steps');
  }

  let finalAnswer='';
  const apiLetter=(apiData.final_answer_letter||'').trim().toUpperCase();
  if(apiLetter&&'ABCD'.includes(apiLetter))finalAnswer=apiLetter;
  else if(typeof apiData.final_answer_index==='number' && apiData.final_answer_index>=0 && apiData.final_answer_index<4)finalAnswer='ABCD'[apiData.final_answer_index];
  if(!finalAnswer)finalAnswer=cGold;

  const steps = apiData.steps.map((s, si) => ({
    t: s.title || s.t || `Step ${si+1}`,
    tx: s.text || s.tx || '',
    correct: null
  }));

  return {id:`sol_${idx+1}`,ok:finalAnswer===cGold,fa:finalAnswer,steps};
}

async function verifySolutionSteps(c, sol){
  const payload={
    question:c.question||c.q||'',
    options:Array.isArray(c.options||c.opts)?(c.options||c.opts):Object.values(c.options||c.opts||{}),
    gold:typeof c.g==='number'?c.g:'ABCD'.indexOf(gold),
    case_type:c.case_type||c.nm||'Medical',
    modality:c.modality||c.mt||'',
    steps:sol.steps.map(step=>({title:step.t,text:step.tx}))
  };

  const res=await fetch('http://localhost:8764/verify-steps',{
    method:'POST',
    headers:{'Content-Type':'application/json'},
    body:JSON.stringify(payload)
  });
  if(!res.ok){
    const errorText=await res.text();
    throw new Error(`step verification failed (${res.status}): ${errorText}`);
  }

  const data=await res.json();
  if(!Array.isArray(data.results)||data.results.length!==sol.steps.length){
    throw new Error('step verification failed: invalid verifier response');
  }

  return data.results.map((r,idx)=>({
    si:idx,
    title:sol.steps[idx].t,
    text:sol.steps[idx].tx,
    cnt:Math.round((r.score||0)*parseInt(document.getElementById('rK').value,10)),
    k:parseInt(document.getElementById('rK').value,10),
    score:Math.max(0,Math.min(1,Number(r.score)||0)),
    label:r.label==='+'?'+':'-',
    rationale:r.rationale||'',
    mode:'openai'
  }));
}

