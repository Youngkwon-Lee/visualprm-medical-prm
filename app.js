function renderMCResults(){
  const solution=sols[activeMcSol];
  const results=mcBySol[activeMcSol]||[];
  const k=parseInt(document.getElementById('rK').value,10);
  if(!solution||!results.length){
    document.getElementById('mcOut').style.display='none';
    return;
  }
  document.getElementById('mcInfo').textContent=mcStrategy==='rollout'
    ? `Solution ${activeMcSol+1}: ${solution.steps.length} steps, ${k} rollouts per prefix. mc means final-answer success rate from that prefix.`
    : `Solution ${activeMcSol+1}: ${solution.steps.length} steps scored with the quick verifier.`;
  document.getElementById('mcOut').style.display='block';
  renderSolutionStageTabs('mcSolTabs');
  document.getElementById('mcSteps').innerHTML=results.map((r,si)=>{
    const badges=Array.from({length:r.k},(_,i)=>`<div class="mc-c ${i<r.cnt?'hit':'miss'}">${i<r.cnt?'O':'X'}</div>`).join('');
    const percent=(r.score*100).toFixed(1);
    const modeText=mcStrategy==='rollout'
      ? 'Paper-style rollout using final-answer correctness.'
      : 'Quick verification using a verifier-style step estimate.';
    return `<div class="mc-blk"><div class="mc-h"><div class="sl-n">${si+1}</div><div style="flex:1;font-size:12px;font-weight:600;">${r.title}</div><div><span class="lb-tag ${r.label==='+'?'pos':'neg'}">${labelText(r.label)}</span></div></div>
    <div class="mc-b"><div style="font-size:10px;color:var(--t4);font-family:var(--mono);margin-bottom:6px;">${modeText}</div>
    <div class="mc-g">${badges}</div>
    <div class="sc-bar"><div class="sc-fill ${r.score>0.6?'hi':r.score>0.3?'md':'lo'}" style="width:${percent}%"></div></div>
    <div class="sc-row"><span>mc=${r.cnt}/${r.k}</span><span>${r.score.toFixed(2)} | ${r.mode||'heuristic'}</span></div>
    <div style="margin-top:4px;font-size:11px;color:var(--t2);">Auto verdict: <strong>${labelText(r.label)}</strong> | Success rate ${percent}%</div>
    <div style="margin-top:6px;font-size:11px;color:var(--t3);">${r.rationale||''}</div></div></div>`;
  }).join('');
}

