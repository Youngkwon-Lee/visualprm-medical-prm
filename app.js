function mkReasoningFallback(c, idx, ok, cGold, wl){
  const optsRaw=c.options||c.opts||[];
  const opts=Array.isArray(optsRaw)?optsRaw:Object.values(optsRaw||{});
  const type=(c.case_type||c.nm||'Medical').toLowerCase();
  const modality=(c.modality||c.mt||'medical image').toLowerCase();
  const question=(c.question||c.q||'').trim();
  const goldIdx='ABCD'.indexOf(cGold);
  const wrongLetter=wl[idx%wl.length]||wl[0]||'A';
  const wrongIdx='ABCD'.indexOf(wrongLetter);
  const finalLetter=ok?cGold:wrongLetter;
  const finalIdx='ABCD'.indexOf(finalLetter);
  const finalText=finalIdx>=0&&finalIdx<opts.length?opts[finalIdx]:finalLetter;
  const goldText=goldIdx>=0&&goldIdx<opts.length?opts[goldIdx]:cGold;
  const isBinary=opts.length===2&&opts.every(o=>['yes','no'].includes(String(o).trim().toLowerCase()));
  const isRadiology=type.includes('radio')||modality.includes('ct')||modality.includes('mri')||modality.includes('x-ray');
  const isPathology=type.includes('path')||modality.includes('h&e');
  const asksPresence=/\b(is|are|does|do|can)\b/i.test(question);
  const asksLocation=/where|which|side|region|lobe|quadrant/i.test(question);
  const asksCount=/how many|number of|count/i.test(question);
  const style = [
    {name:'Systematic Read', opener:'A systematic read starts with the overall image context before narrowing to the specific target finding.'},
    {name:'Evidence Chain', opener:'This pass builds a short evidence chain from visible clue to diagnostic implication.'},
    {name:'Differential Pass', opener:'This pass compares the leading interpretation against the most plausible alternatives.'},
    {name:'Decision Audit', opener:'This pass audits the answer choice against each major clue before committing.'}
  ][idx%4];

  const observationLine=isPathology
    ? `${style.opener} The slide should first be scanned for tissue architecture, staining pattern, and any focal abnormal structures.`
    : isRadiology
      ? `${style.opener} The image should first be reviewed for laterality, projection, overall quality, and the dominant radiologic abnormality.`
      : `${style.opener} The image should first be reviewed globally before focusing on the finding most relevant to the question.`;
  const detailLine=asksLocation
    ? 'The next step is to localize the abnormality and confirm whether the described anatomic region matches the visual evidence.'
    : asksCount
      ? 'The next step is to estimate the number of visible abnormalities and check whether the options match that burden.'
      : asksPresence
        ? 'The next step is to look for direct visual evidence for the queried finding and for any secondary signs that support it.'
        : 'The next step is to connect the visible findings to the answer choices and remove options that conflict with the image.';
  const supportLine=[
    ok
      ? `These findings fit best with ${finalText}, and they are more consistent with that option than with the alternatives.`
      : `A superficial read could over-weight ${finalText}, but that interpretation is weaker than the evidence supporting ${goldText}.`,
    ok
      ? `${finalText} is favored because the visible pattern and the question stem point in the same direction.`
      : `${finalText} may sound tempting, but the image does not support it as directly as ${goldText}.`,
    ok
      ? `When competing answers are compared one by one, ${finalText} remains the most defensible choice.`
      : `A differential comparison suggests that ${finalText} drops behind ${goldText} once the stronger clues are prioritized.`,
    ok
      ? `An audit of the evidence leaves ${finalText} as the option with the fewest contradictions.`
      : `An audit of the evidence exposes gaps in choosing ${finalText}, especially compared with ${goldText}.`
  ][idx%4];
  const cautionLine=[
    ok
      ? 'The distractor choices are less convincing because they do not explain the dominant visual pattern as well.'
      : 'This reasoning remains vulnerable because it relies on an incomplete feature match and does not fully resolve competing explanations.',
    ok
      ? 'Negative clues also matter here: the alternatives would require findings that are not strongly visible.'
      : 'The weakness in this line of thought is that it overuses a partial clue while ignoring missing corroboration.',
    ok
      ? 'The preferred option survives both positive evidence and the absence of stronger support for the distractors.'
      : 'A better differential would keep the stronger alternative higher because it explains more of the image.',
    ok
      ? 'Cross-checking the answer against the prompt does not reveal any major inconsistency.'
      : 'Cross-checking the answer against the prompt reveals a mismatch between the claimed finding and the visible evidence.'
  ][idx%4];
  const conclusionLine=isBinary
    ? `Final answer: ${finalText}. The overall image pattern is ${ok?'supportive':'only partially supportive'} of that yes/no decision.`
    : `Final answer: ${finalLetter} (${finalText}). This option is ${ok?'the best match for the visual findings':'a plausible but ultimately weaker match for the visual findings'}.`;

  return [
    {t:style.name,tx:observationLine,correct:true},
    {t:idx%2===0?'Targeted Findings':'Visual Cross-check',tx:detailLine,correct:true},
    {t:idx%2===0?'Option Matching':'Evidence Weighing',tx:supportLine,correct:ok},
    {t:idx%2===0?'Diagnostic Check':'Risk Check',tx:cautionLine,correct:ok},
    {t:'Conclusion',tx:conclusionLine,correct:ok}
  ];
}

async function mkSol(c,idx,tot,cGold){
  cGold=cGold||gold;
  const fallbackOk=idx<Math.ceil(tot*[1,.75,.5,.25][Math.min(idx,3)]);
  const wl='ABCD'.split('').filter(l=>l!==cGold);
  const genParams=getGenerationParams(idx);

  let steps=[];
  let finalAnswer='';

  if(generationMode==='api'){
    try {
      // GPT-4o API???????????????????????????????????????????????????????????썹땟戮녹??諭?????⑸㎦???????????????????饔낅떽????묐뻿????ㅼ굣?????????????????????????椰???????????????????????嶺??????????????????????????????
      const apiRes = await fetch('http://localhost:8764/generate-steps', {
        method: 'POST',
        headers: {'Content-Type': 'application/json'},
        body: JSON.stringify({
          question: c.question || c.q || '',
          options: c.options || c.opts || [],
          gold: cGold === 'A' ? 0 : cGold === 'B' ? 1 : cGold === 'C' ? 2 : cGold === 'D' ? 3 : 0,
          dataset: c.dataset || 'pathvqa',
          case_type: c.case_type || c.nm || 'Medical',
          temperature: genParams.temperature,
          top_p: genParams.top_p
        })
      });

      if (apiRes.ok) {
        const apiData = await apiRes.json();
        if (apiData.steps && apiData.steps.length) {
          const apiLetter=(apiData.final_answer_letter||'').trim().toUpperCase();
          if(apiLetter&&'ABCD'.includes(apiLetter))finalAnswer=apiLetter;
          else if(typeof apiData.final_answer_index==='number' && apiData.final_answer_index>=0 && apiData.final_answer_index<4)finalAnswer='ABCD'[apiData.final_answer_index];
          steps = apiData.steps.map((s, si) => ({
            t: s.title || s.t || `Step ${si+1}` ,
            tx: s.text || s.tx || '',
            correct: null
          }));
        }
      }
    } catch (e) {
      console.warn('API call failed, falling back to prototype:', e);
    }
  }

  // API ???????????????????????????ㅻ깹????????????????????????????????????????????????????????????????????????????????????????????????꾩룆梨띰쭕?뚢뵾??????????????嶺뚮죭?댁젘??????????????????????釉먮폁???????????????????살몝??????????????????????????????????????????????????????????????????????????????????썹땟戮녹??諭?????⑸㎦???????????????????饔낅떽????묐뻿????ㅼ굣?????????????????????????椰???????????????????????嶺????????????
  if (!steps.length) {
    steps=mkReasoningFallback(c,idx,fallbackOk,cGold,wl);
    finalAnswer=fallbackOk?cGold:wl[idx%wl.length];
  }

  if(!finalAnswer)finalAnswer=cGold;

  return{id:`sol_${idx+1}`,ok:finalAnswer===cGold,fa:finalAnswer,steps};
}

